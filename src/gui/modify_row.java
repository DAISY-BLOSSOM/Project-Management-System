    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == del) {
            int emp_id = Integer.parseInt(id.getText());
            try {
                Connection c = database.employee_db.connect();
                Statement sc = c.createStatement();
                String sql = "delete from employee where emp_id='" + emp_id + "'";
                int rs = sc.executeUpdate(sql);
                if (rs == 1) {
                    JOptionPane.showMessageDialog(null, "record has been deleted succesfully", "Delete", JOptionPane.INFORMATION_MESSAGE);
                    new admin_update_data().show_update_delete();
                    this.dispose();
                }

            } catch (SQLException ee) {
                System.out.println(ee.getMessage());
            }
        }
    }//GEN-LAST:event_delActionPerformed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == save) {
            int emp_id = Integer.parseInt(id.getText());
            try {
                Connection c = database.employee_db.connect();
                Statement sc = c.createStatement();
                String sql = "update employee set emp_name='" + name.getText() + "', password = '" + pass.getText() + "', position='" + pos.getText() + "', salary='" + Double.parseDouble(salary.getText()) + "' , hours_per_month='" + Double.parseDouble(hrs.getText()) + "',left_vacation='" + Integer.parseInt(left.getText()) + "',p_vacation='" + Integer.parseInt(vp.getText()) + "',p_being_late='" + Integer.parseInt(lp.getText()) + "',repoet='" + report.getText() + "',task_id='" + Integer.parseInt(taskid.getText()) + "'    where emp_id = '" + emp_id + "'";
                int rs = sc.executeUpdate(sql);
                if (rs == 1) {
                    JOptionPane.showMessageDialog(null, "record has been updated succesfully", "update", JOptionPane.INFORMATION_MESSAGE);
                    new admin_update_data().show_update_delete();
                    this.dispose();
                }

            } catch (SQLException ee) {
                JOptionPane.showMessageDialog(null, "Fill all the information \n if you don't know one of them set it to 0", "Warning", JOptionPane.WARNING_MESSAGE);

                System.out.println(ee.getMessage());
            }
        }
    }//GEN-LAST:event_saveActionPerformed

