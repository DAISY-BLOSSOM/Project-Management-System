    public modify_row(int value) {
        initComponents();
        id.setText(value + "");
        try {
            Connection c = database.employee_db.connect();
            Statement sc = c.createStatement();
            String sql = "select * from employee where emp_id='" + value + "'";
            ResultSet rs = sc.executeQuery(sql);
            while (rs.next()) {
                name.setText(rs.getString("emp_name"));
                pass.setText(rs.getString("password"));
                pos.setText(rs.getString("position"));
                salary.setText(rs.getString("salary"));
                hrs.setText(rs.getString("hours_per_month"));
                left.setText(rs.getString("left_vacation"));
                vp.setText(rs.getString("p_vacation"));
                lp.setText(rs.getString("p_being_late"));
                report.setText(rs.getString("repoet"));
                taskid.setText(rs.getString("task_id"));

            }
        } catch (SQLException ee) {
            System.out.println(ee.getMessage());
        }

    }

