    public Make_report(int value) {
        initComponents();
        id.setText(value + "");
        try {
            Connection c = database.employee_db.connect();
            Statement sc = c.createStatement();
            String sql = "select emp_id,emp_name,repoet from employee where emp_id='" + value + "'";
            ResultSet rs = sc.executeQuery(sql);
            while (rs.next()) {
                report.setText(rs.getString("repoet"));
            }
        } catch (SQLException ee) {
            System.out.println(ee.getMessage());
        }
    }

