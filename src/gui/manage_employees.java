    public manage_employees(int value) {
        initComponents();
        id.setText(value + "");
        try {
            Connection c = database.employee_db.connect();
            Statement sc = c.createStatement();
            String sql = "select salary,p_vacation , p_being_late from employee where emp_id='" + value + "'";
            ResultSet rs = sc.executeQuery(sql);
            while (rs.next()) {
                sal.setText(rs.getString("salary"));
                vac.setText(rs.getString("p_vacation"));
                late.setText(rs.getString("p_being_late"));
            }
        } catch (SQLException ee) {
            System.out.println(ee.getMessage());
        }

    }

