    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == send) {
             int emp_id = Integer.parseInt(id.getText());
            try {
                Connection c = database.employee_db.connect();
                Statement sc = c.createStatement();
                String sql = "update employee set  repoet='" + report.getText() + "'    where emp_id = '" + emp_id + "'";
                int rs = sc.executeUpdate(sql);
                if (rs == 1) {
                    JOptionPane.showMessageDialog(null, "record has been updated succesfully", "update", JOptionPane.INFORMATION_MESSAGE);
                    new report().show_report();
                    this.dispose();
                }

            } catch (SQLException ee) {
                System.out.println(ee.getMessage());
            }


        }
    }//GEN-LAST:event_sendActionPerformed

