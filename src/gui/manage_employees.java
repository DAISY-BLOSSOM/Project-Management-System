    private void upActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == up) {
            int emp_id = Integer.parseInt(id.getText());
            try {
                Connection c = database.employee_db.connect();
                Statement sc = c.createStatement();
                String sql = "update employee set   salary='" + Double.parseDouble(sal.getText()) + "' ,p_vacation='" + Integer.parseInt(vac.getText()) + "',p_being_late='" + Integer.parseInt(late.getText()) + "'  where emp_id = '" + emp_id + "'";
                int rs = sc.executeUpdate(sql);
                if (rs == 1) {
                    JOptionPane.showMessageDialog(null, "record has been updated succesfully", "Manage", JOptionPane.INFORMATION_MESSAGE);
                    new manage1().show_manage_table();
                    this.dispose();
                }

            } catch (SQLException ee) {
                System.out.println(ee.getMessage());
            }
        }
    }//GEN-LAST:event_upActionPerformed

